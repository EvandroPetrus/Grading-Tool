public class Alumni {
    private String nameAlumni;
    private int yearAlumni;
    private double gradeAlumni;

    public String getNameAlumni() {
        return nameAlumni;
    }

    public void setNameAlumni(String nameAlumni) {
        this.nameAlumni = nameAlumni;
    }

    public int getYearAlumni() {
        return yearAlumni;
    }

    public void setYearAlumni(int ageAlumni) {
        this.yearAlumni = ageAlumni;
    }

    public double getGradeAlumni() {
        return gradeAlumni;
    }

    public void setGradeAlumni(double gradeAlumni) {
        this.gradeAlumni = gradeAlumni;
    }
}
